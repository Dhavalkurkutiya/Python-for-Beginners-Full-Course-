# 1. What is type casting?
# Ans: Type casting is the process of converting the data type of a variable to another data type.

# 2. What are the types of type casting?
# Ans: The types of type casting are:
# 1. Implicit Type Casting
# 2. Explicit Type Casting

# 3. What is implicit type casting?
# Ans: Implicit type casting is the process of converting the data type of a variable to another data type automatically by the compiler.

# 4. What is explicit type casting?
# Ans: Explicit type casting is the process of converting the data type of a variable to another data type manually by the programmer.


# 1. Implicit Type Casting
# Example:
a = 12
b = 12.5

print(a + b)
print(type(a + b))
