# 1. What is for loops ?
# Ans: The for loop in Python is used to iterate over a sequence (list, tuple, string) or other iterable objects. Iterating over a sequence is called traversal.

colors = ["Red", "Green", "Blue", "Yellow", "White", "Black"]

for i in colors:
    print(i)

for i in range(0,100,2):
    print(i+1)